    public String getTextoBotaoSelecionado(ButtonGroup grupo)
    {
        Enumeration<AbstractButton> botoes = grupo.getElements();
        
        while(botoes.hasMoreElements())
        {
            AbstractButton botao = botoes.nextElement();
            
            if(botao.isSelected())
                return botao.getText();
        }
        
        return null;
    }

